def add(a, b): # No docstring
    """```python
    Adds two numbers together.
    
    Args:
      a: The first number.  Must be a number type (int, float, etc.).
      b: The second number. Must be a number type (int, float, etc.).
    
    Returns:
      The sum of a and b.  Returns the same numeric type as the input numbers if 
      they are of the same type; otherwise, returns a float.
    
    Raises:
      TypeError: If either input is not a number.
    
    ```
    """
    return a + b

def subtract(a, b):
    """Subtracts b from a."""
    return a - b

def multiply(a, b): # No docstring
    """```python
    Multiplies two numbers.
    
    Args:
      a: The first number.  Must be a number type (int or float).
      b: The second number. Must be a number type (int or float).
    
    Returns:
      The product of a and b. Returns an int if both inputs are ints; otherwise, returns a float.
    
    Raises:
      TypeError: If either input is not a number.
    
    ```
    """
    return a * b
