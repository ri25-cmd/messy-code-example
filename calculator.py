def add(a, b): # No docstring
    """```python
    Adds two numbers together.
    
    Args:
      a: The first number.  Must be a number.
      b: The second number. Must be a number.
    
    Returns:
      The sum of a and b.  Returns an error if either input is not a number.
    
    Raises:
      TypeError: If either input is not a number.
    
    ```
    """
    return a + b

def subtract(a, b):
    """Subtracts b from a."""
    return a - b

def multiply(a, b): # No docstring
    """```python
    Multiplies two numbers.
    
    Args:
      a: The first number.  Must be a number (int or float).
      b: The second number. Must be a number (int or float).
    
    Returns:
      The product of a and b. Returns a float if either input is a float, otherwise an integer.
    
    Raises:
      TypeError: If either input is not a number.
    
    ```
    """
    return a * b
